package com.spring.dto;

public class AdminVO {

	String adminId;
	String adminEmail;
	String adminPwd;
	String adminName;
	String adminAuthority;
	String adminQuest;
	String adminAns;
	
	
	public String getAdminQuest() {
		return adminQuest;
	}
	public void setAdminQuest(String adminQuest) {
		this.adminQuest = adminQuest;
	}
	public String getAdminAns() {
		return adminAns;
	}
	public void setAdminAns(String adminAns) {
		this.adminAns = adminAns;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getAdminEmail() {
		return adminEmail;
	}
	public void setAdminEmail(String adminEmail) {
		this.adminEmail = adminEmail;
	}
	public String getAdminPwd() {
		return adminPwd;
	}
	public void setAdminPwd(String adminPwd) {
		this.adminPwd = adminPwd;
	}
	public String getAdminName() {
		return adminName;
	}
	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}
	public String getAdminAuthority() {
		return adminAuthority;
	}
	public void setAdminAuthority(String adminAuthority) {
		this.adminAuthority = adminAuthority;
	}
	
	
}
